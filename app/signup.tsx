import CustomButton from "@/components/CustomButton";
import CustomHeader from "@/components/CustomHeader";
import CustomInput from "@/components/CustomInput";
import ScreenWrapper from "@/components/ScreenWrapper";
import { useAuth } from "@/contexts/AuthContext";
import { useLanguage } from "@/contexts/LanguageContext";
import { FontAwesome } from "@expo/vector-icons";
import * as AppleAuthentication from 'expo-apple-authentication';
import { useRouter } from "expo-router";
import { useState } from "react";
import { Platform, ScrollView, StyleSheet, Text, TouchableOpacity, View } from "react-native";
import Toast from "react-native-toast-message";

export default function SignupScreen() {
  const router = useRouter();
  const { register, signInWithGoogle, signInWithFacebook, signInWithApple } = useAuth();
  const { t } = useLanguage();

  const [name, setName] = useState("");
  const [email, setEmail] = useState("");
  const [mobile, setMobile] = useState("");
  const [password, setPassword] = useState("");
  const [showPassword, setShowPassword] = useState(false);
  const [loading, setLoading] = useState(false);

  const handleSignup = async () => {
    if (!name || !email || !mobile || !password) {
      Toast.show({
        type: "error",
        text1: t("error"),
        text2: t("all_fields_mandatory") || "All fields are required",
      });
      return;
    }
    // Email Validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      Toast.show({
        type: "error",
        text1: t("invalid_email") || "Invalid Email",
        text2: t("enter_valid_email") || "Please enter a valid email address",
      });
      return;
    }

    // Smart Check for common typos
    if (email.toLowerCase().endsWith("@gmail.co")) {
      Toast.show({
        type: "info",
        text1: "Did you mean @gmail.com?",
        text2: "It looks like you typed '@gmail.co' instead of '@gmail.com'.",
      });
      return;
    }

    // Mobile Validation: Exactly 10 digits
    const mobileRegex = /^\d{10}$/;
    if (!mobileRegex.test(mobile)) {
      Toast.show({
        type: "error",
        text1: t("invalid_mobile"),
        text2: t("mobile_10_digits") || "Mobile number must be 10 digits",
      });
      return;
    }

    // Password Validation: Min 8 chars, 1 letter, 1 number, 1 special char
    const trimmedPassword = password.trim();
    const passwordRegex = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]).{8,}$/;

    if (!passwordRegex.test(trimmedPassword)) {
      Toast.show({
        type: "error",
        text1: t("weak_password"),
        text2: t("password_requirements") || "Password must be at least 8 characters, with 1 uppercase, 1 lowercase, 1 number, and 1 special char.",
        visibilityTime: 5000, // Longer time to read
      });
      return;
    }

    setLoading(true);
    try {
      // BACKEND OTP FLOW
      await register(name, email, mobile, password); // Calls backend /auth/signup (generates OTP)

      // Navigate to OTP screen
      Toast.show({
        type: "success",
        text1: "OTP Sent",
        text2: `Verification code sent to ${email}`,
        visibilityTime: 4000,
      });

      router.push({
        pathname: "/otp",
        params: { name, email, mobile, password }
      });

    } catch (error: any) {
      console.error("Signup Error Details:", error);

      // Determine the error message string
      let errorMessage = t("error");
      if (typeof error === 'string') {
        errorMessage = error;
      } else if (error?.response?.data?.message) {
        errorMessage = error.response.data.message;
      } else if (error?.message) {
        errorMessage = error.message;
      }

      console.log("Final Error Message:", errorMessage); // Debugging

      if (errorMessage.toLowerCase().includes("already exists")) {
        if (Platform.OS === 'web') {
          Toast.show({
            type: "error",
            text1: t("account_already_exists"),
            text2: t("account_exists_msg"),
          });
          setTimeout(() => {
            router.push("/login");
          }, 1500);
        } else {
          Toast.show({
            type: "error",
            text1: t("account_already_exists"),
            text2: t("account_exists_msg"),
          });
          setTimeout(() => {
            router.push("/login"); // Navigate to login
          }, 1500);
        }
      } else {
        Toast.show({
          type: "error",
          text1: t("signup_failed"),
          text2: errorMessage,
        });
      }
    } finally {
      setLoading(false);
    }
  };

  const handleAppleLogin = async () => {
    try {
      await signInWithApple();
      router.replace("/(tabs)");
    } catch (error: any) {
      if (error === 'Cancelled') return;
      console.error("Apple Sign-In Error:", error);
      Toast.show({
        type: "error",
        text1: "Apple Login Failed",
        text2: error.message || "Something went wrong",
      });
    }
  };

  const handleGoogleLogin = async () => {
    try {
      await signInWithGoogle();
      router.replace("/(tabs)");
    } catch (error: any) {
      console.error("Google Sign-In Error:", error);
      if (error.code === 'SIGN_IN_CANCELLED') return;
      Toast.show({
        type: "error",
        text1: "Google Login Failed",
        text2: error.message || "Something went wrong",
      });
    }
  };

  return (
    <ScreenWrapper>
      <CustomHeader />

      <ScrollView
        contentContainerStyle={styles.scrollContainer}
        showsVerticalScrollIndicator={false}
      >
        {/* Titles */}
        <Text style={styles.title}>{t("create_account") || "Create Account"}</Text>
        <Text style={styles.subtitle}>{t("fill_details") || "Fill your details or continue with social media"}</Text>

        {/* Inputs */}
        <CustomInput
          label={t("name") || "Your Name"}
          placeholder="Ex: Jhon Doe"
          value={name}
          onChangeText={setName}
        />

        <CustomInput
          label={t("email_address") || "Email Address"}
          placeholder="Ex: jhondoe@gmail.com"
          value={email}
          onChangeText={setEmail}
          autoCapitalize="none"
          keyboardType="email-address"
        />

        <CustomInput
          label={t("phone_number") || "Mobile Number"}
          placeholder="9999999999"
          value={mobile}
          onChangeText={setMobile}
          keyboardType="phone-pad"
          maxLength={10}
          prefix="+91"
        />

        <CustomInput
          label={t("password") || "Password"}
          placeholder="********"
          value={password}
          onChangeText={setPassword}
          secureTextEntry={!showPassword}
          rightIcon={showPassword ? "eye" : "eye-off"}
          onRightIconPress={() => setShowPassword(!showPassword)}
        />

        {/* Sign Up Button */}
        <CustomButton
          title={loading ? t("signing_up") || "Creating Account..." : t("sign_up") || "Sign Up"}
          onPress={handleSignup}
          loading={loading}
        />

        {/* Divider */}
        <Text style={styles.divider}>OR</Text>

        {/* Apple */}
        {Platform.OS === 'ios' && (
          <AppleAuthentication.AppleAuthenticationButton
            buttonType={AppleAuthentication.AppleAuthenticationButtonType.CONTINUE}
            buttonStyle={AppleAuthentication.AppleAuthenticationButtonStyle.BLACK}
            cornerRadius={30}
            style={{ width: '100%', height: 50, marginBottom: 12 }}
            onPress={handleAppleLogin}
          />
        )}
        {Platform.OS === 'web' && (
          <TouchableOpacity style={[styles.socialButton, { backgroundColor: 'black', borderColor: 'black' }]} onPress={handleAppleLogin}>
            <FontAwesome name="apple" size={22} color="white" style={{ marginRight: 10 }} />
            <Text style={[styles.socialText, { color: 'white' }]}>{t("continue_apple") || "Continue with Apple"}</Text>
          </TouchableOpacity>
        )}

        {/* Google */}
        <TouchableOpacity style={styles.socialButton} onPress={handleGoogleLogin}>
          <FontAwesome name="google" size={22} color="#DB4437" style={{ marginRight: 10 }} />
          <Text style={styles.socialText}>{t("continue_google") || "Continue with Google"}</Text>
        </TouchableOpacity>

        {/* Facebook */}
        <TouchableOpacity style={styles.socialButton} onPress={async () => {
          try {
            await signInWithFacebook();
            router.replace("/(tabs)");
          } catch (error: any) {
            console.error("Facebook Sign-In Error:", error);
            Toast.show({
              type: "error",
              text1: "Facebook Login Failed",
              text2: error.message,
            });
          }
        }}>
          <View style={{
            width: 24,
            height: 24,
            borderRadius: 12,
            backgroundColor: "#1877F2",
            justifyContent: "center",
            alignItems: "center",
            marginRight: 10
          }}>
            <FontAwesome
              name="facebook"
              size={16}
              color="#FFF"
            />
          </View>
          <Text style={styles.socialText}>{t("continue_facebook") || "Continue with Facebook"}</Text>
        </TouchableOpacity>

        {/* Bottom Link */}
        <Text style={styles.bottomText}>
          {t("already_have_account") || "Already have an account?"}{" "}
          <Text
            style={styles.link}
            onPress={() => router.push("/login")}
          >
            {t("login") || "Log In"}
          </Text>
        </Text>
      </ScrollView>
    </ScreenWrapper>
  );
}

const styles = StyleSheet.create({
  scrollContainer: {
    padding: 24,
    flexGrow: 1,
  },
  title: {
    fontSize: 28,
    fontWeight: "700",
    marginTop: 10,
    marginBottom: 5,
    color: "#000",
  },
  subtitle: {
    fontSize: 16,
    color: "#6F6F6F",
    marginBottom: 25,
  },
  divider: {
    textAlign: "center",
    marginVertical: 20,
    color: "#6F6F6F",
    fontSize: 16,
  },
  socialButton: {
    flexDirection: "row",
    alignItems: "center",
    justifyContent: "center",
    paddingVertical: 12, // Reduced height a bit to match design usually
    borderWidth: 1,
    borderColor: "#E6E6E6",
    borderRadius: 30, // Rounded full
    marginBottom: 12,
    backgroundColor: 'white',
  },
  socialText: {
    fontSize: 16,
    color: "#000",
    fontWeight: "600",
  },
  bottomText: {
    textAlign: "center",
    marginTop: 20,
    fontSize: 16, // Matched other screens
    color: "#6F6F6F",
    marginBottom: 40,
  },
  link: {
    color: "#000",
    fontWeight: "700",
  },
});
