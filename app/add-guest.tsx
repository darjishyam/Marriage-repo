
import { Ionicons } from "@expo/vector-icons";
import { KeyboardAvoidingView, Platform, ScrollView, StyleSheet, Text, TextInput, TouchableOpacity, View } from "react-native";
import { SafeAreaView } from "react-native-safe-area-context";

import { useGuest } from "@/contexts/GuestContext";
import { useLanguage } from "@/contexts/LanguageContext";
import { useLocalSearchParams, useRouter } from "expo-router";
import { useEffect, useState } from "react";
import Toast from "react-native-toast-message";

export default function AddGuestScreen() {
  const router = useRouter();
  const params = useLocalSearchParams();
  const { id } = params;
  const isEdit = !!id;

  const { addGuest, guests, updateGuest } = useGuest();
  const { t } = useLanguage();

  const [name, setName] = useState("");
  const [totalFamilyCount, setTotalFamilyCount] = useState("");
  const [cityVillage, setCityVillage] = useState("");
  const [loading, setLoading] = useState(false);

  useEffect(() => {
    if (isEdit && guests) {
      const guest = guests.find((g: any) => g._id === id);
      if (guest) {
        setName(guest.name);
        setTotalFamilyCount(guest.familyCount.toString());
        setCityVillage(guest.cityVillage);
      }
    }
  }, [id, guests]);

  const resetForm = () => {
    setName("");
    setTotalFamilyCount("");
    setCityVillage("");
  };

  const handleSaveCommon = async (shouldGoBack: boolean) => {
    if (!name.trim() || !totalFamilyCount.trim() || !cityVillage.trim()) {
      Toast.show({
        type: "error",
        text1: t("error"),
        text2: t("all_fields_mandatory"),
      });
      return;
    }

    setLoading(true);
    try {
      if (isEdit) {
        await updateGuest(id as string, { name, count: parseInt(totalFamilyCount), city: cityVillage });
        Toast.show({
          type: "success",
          text1: t("success"),
          text2: t("guest_updated_success") || "Guest updated successfully",
        });
      } else {
        await addGuest(name, parseInt(totalFamilyCount), cityVillage);
        Toast.show({
          type: "success",
          text1: t("success"),
          text2: t("guest_added_success"),
        });
      }

      if (shouldGoBack || isEdit) {
        // Redirect to Home Page or Back as requested
        router.navigate("/(tabs)");
      } else {
        resetForm();
      }
    } catch (error) {
      Toast.show({
        type: "error",
        text1: t("error"),
        text2: t("failed_add_guest"),
      });
    } finally {
      setLoading(false);

    }
  }

  const handleSaveAndAddAnother = () => handleSaveCommon(false);
  const handleSave = () => handleSaveCommon(true);

  return (
    <SafeAreaView style={styles.container}>
      <KeyboardAvoidingView
        style={{ flex: 1 }}
        behavior={Platform.OS === "ios" ? "padding" : "height"}
        keyboardVerticalOffset={Platform.OS === "ios" ? 0 : 20}
      >
        {/* Navigation Bar */}
        <View style={styles.navBar}>
          <TouchableOpacity
            style={styles.backButton}
            onPress={() => router.back()}
          >
            <Ionicons name="arrow-back" size={24} color="#000" />
          </TouchableOpacity>
          <Text style={styles.navTitle}>{isEdit ? t("edit_guest") || "Edit Guest" : t("add_new_guest")}</Text>
          <View style={styles.placeholder} />
        </View>

        {/* Form Content */}
        <ScrollView
          style={styles.scrollView}
          contentContainerStyle={styles.scrollContent}
          showsVerticalScrollIndicator={false}
          keyboardShouldPersistTaps="handled"
        >
          {/* Name Field */}
          <View style={styles.inputGroup}>
            <Text style={styles.label}>{t("name")}</Text>
            <TextInput
              style={[styles.input, Platform.OS === 'web' && ({ outlineStyle: 'none' } as any)]}
              value={name}
              onChangeText={setName}
              placeholder="Moon"
              placeholderTextColor="#999"
            />
          </View>

          {/* Total family Count Field */}
          <View style={styles.inputGroup}>
            <Text style={styles.label}>{t("total_family_count")}</Text>
            <TextInput
              style={[styles.input, Platform.OS === 'web' && ({ outlineStyle: 'none' } as any)]}
              value={totalFamilyCount}
              onChangeText={setTotalFamilyCount}
              placeholder="5"
              placeholderTextColor="#999"
              keyboardType="numeric"
            />
          </View>

          {/* City/Village Field */}
          <View style={styles.inputGroup}>
            <Text style={styles.label}>{t("city_village")}</Text>
            <TextInput
              style={[styles.input, Platform.OS === 'web' && ({ outlineStyle: 'none' } as any)]}
              value={cityVillage}
              onChangeText={setCityVillage}
              placeholder="Surat"
              placeholderTextColor="#999"
            />
          </View>
        </ScrollView>

        {/* Action Buttons */}
        <View style={styles.buttonContainer}>
          {!isEdit && (
            <TouchableOpacity
              style={[styles.saveAndAddButton, loading && { opacity: 0.7 }]}
              onPress={handleSaveAndAddAnother}
              disabled={loading}
            >
              <Text style={styles.saveAndAddButtonText}>{t("save_and_add_another")}</Text>
            </TouchableOpacity>
          )}

          <TouchableOpacity
            style={[styles.saveButton, loading && { opacity: 0.7 }]}
            onPress={handleSave}
            disabled={loading}
          >
            <Text style={styles.saveButtonText}>{loading ? (isEdit ? "Updating..." : "Saving...") : (isEdit ? t("update") || "Update" : t("save"))}</Text>
          </TouchableOpacity>
        </View>
      </KeyboardAvoidingView>
    </SafeAreaView>
  );
}

